"""This module provides a simple calculator for demo purpose."""

class Calculator:
    """A simple calculator."""
    def __init__(self) -> None:
        pass

    def add(self, x: int, y: int) -> int:
        """Do addition.
        
        Args:
            x(int): first operand.
            y(int): second operand.
        
        Returns:
            int: result of addition.
        """
        return x + y
